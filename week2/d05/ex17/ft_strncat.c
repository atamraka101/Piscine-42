char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (src[0] == '\0')
	{
		return (dest);
	}
	while (dest[i] != '\0')
	{
		i++;
	}
	while ((j < nb) && (src[j] != '\0'))
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}
