char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	if ((str[0] >= 'a') && (str[0] <= 'z'))
	{
		str[0] = str[0] - 32;
	}
	while (str[i] != '\0')
	{
		if ((str[i] >= 0 && str[i] <= 127)
		&& !(str[i] >= '0') && (str[i] <= '9'))
		{
			i++;
			if ((str[i] >= 'a') && (str[i] <= 'z'))
			{
				str[i] = str[i] - 32;
			}
		}
		else
		{
			i++;
		}
	}
	return (str);
}
